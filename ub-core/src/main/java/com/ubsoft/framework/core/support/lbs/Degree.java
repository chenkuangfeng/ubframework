package com.ubsoft.framework.core.support.lbs;

/**
 * 经纬度坐标
 * @author chenkf
 *
 */
public class Degree {

	private double x;
	private double y;
	public Degree(double x, double y) {
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}

	

}
